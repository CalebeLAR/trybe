class ParaFixar01:
    def Exercício01_sum_two_numbers(a, b):
        """Retorna a soma de dois números recebidos por parâmetro.

        Exemplos
        --------
        >>> ParaFixar01.Exercício01_sum_two_numbers(0, 0)
        0
        >>> ParaFixar01.Exercício01_sum_two_numbers(0, 0.0)
        0.0
        >>> ParaFixar01.Exercício01_sum_two_numbers(1, 1.0)
        2.0
        """
        # return a - b
        return a + b
